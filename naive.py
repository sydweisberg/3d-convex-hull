# pts should not include p1, p2, and p3
# p1, p2, and p3 is a face on the polygon
def isExtremeFace(p1, p2, p3, pts):
    firstOrientation = findOrientation(p1, p2, p3, pts[0])
    for i in range(1, len(pts)):
        if findOrientation(p1, p2, p3, pts[i]) != firstOrientation:
            return False
    return True

def findExtremeFaces(pts):
    # for each possible triangle
    extremeFaces = []
    for t in itertools.combinations(pts, 3):
        remaining = [p for p in pts if p not in t]
        # if every pt is on the same side as t its an extreme face
        if isExtremeFace(t[0], t[1], t[2], remaining):
            extremeFaces.append(t)
    return extremeFaces


