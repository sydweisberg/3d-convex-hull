import numpy as np
import itertools

def findOrientation(p1, p2, p3, p4):
    # Use homogeneous coordinates to compute signed volume of tetrahedron
    matrix = np.array([[p1[0], p1[1], p1[2], 1],
                       [p2[0], p2[1], p2[2], 1],
                       [p3[0], p3[1], p3[2], 1],
                       [p4[0], p4[1], p4[2], 1]])
    return np.linalg.det(matrix) / 6

